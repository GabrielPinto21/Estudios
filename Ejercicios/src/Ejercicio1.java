public class Ejercicio1 {
    public static void main(String[] args) {
        double num1=3, num2=4;
        System.out.println(num1+"+"+num2 + "=" + (num1+num2));
        System.out.println(num1+"-"+num2 + "=" + (num1-num2));
        System.out.println(num1+"*"+num2 + "=" + (num1*num2));
        System.out.println(num1+"/"+num2 + "=" + (num1/num2));
        System.out.println(num1+"%"+num2 + "=" + (num1%num2));
    }
}
