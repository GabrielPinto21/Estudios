package Ejercicio6;

public class Concurso {
    private int id;
    private String nombre;
    private String ciudadRealizacion;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCiudadRealizacion() {
        return ciudadRealizacion;
    }

    public void setCiudadRealizacion(String ciudadRealizacion) {
        this.ciudadRealizacion = ciudadRealizacion;
    }
}
